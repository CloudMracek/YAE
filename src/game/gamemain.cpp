GLuint vertexBuffer;
Mesh* mesh1;
GameObject* object;
Texture* texture;

void gameSetup() {
	glGenBuffers(1, &vertexBuffer);
	glBindBuffer(GL_ARRAY_BUFFER, vertexBuffer);
	glBufferData(GL_ARRAY_BUFFER, sizeof(g_vertex_buffer_data), g_vertex_buffer_data, GL_STATIC_DRAW);

    GLuint uvbuffer;
	glGenBuffers(1, &uvbuffer);
	glBindBuffer(GL_ARRAY_BUFFER, uvbuffer);
	glBufferData(GL_ARRAY_BUFFER, sizeof(g_uv_buffer_data), g_uv_buffer_data, GL_STATIC_DRAW);

	mesh1 = new Mesh();
	mesh1->setVertexBuffer(vertexBuffer, 12*3, uvbuffer);

    texture = new Texture("textures/uvtemplate.DDS");
    mesh1->setTexture(texture);

	object =  new GameObject(glm::vec3(0.0f, 0.0f, 0.0f));
	object->setMesh(mesh1);

	Scene scene1 = Scene("Test scene");
	scene1.addObject(object);

	loadScene(scene1);
}

void gameCleanup() {
    delete object;
    delete mesh1;
}

